    {
        if ($request->ajax()) {
            
            $orders = Order::get();

            return DataTables::of($orders)->addIndexColumn()
            ->addcolumn('action', function($row){ $btn = '<a target="_blank" href="'.route("orders.show", [$row->id]).'" class="btn btn-primary">عرض</a>'; return $btn; })
            ->rawColumns(['action'])
            ->addIndexColumn()
            ->make(true);

        }     

        return view('admin.orders.index');
    }

